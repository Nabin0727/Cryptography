# Using extended Euclid algorithm

def modInverse(a, m):
    m0 = m
    y = 0
    x = 1

    if (m == 1):
        return 0

    while (a > 1):
        # q is quotient
        q = a // m
        t = m

        # m is remainder now, process
        # same as Euclid's algo
        m = a % m
        a = t
        t = y

        # Update x and y
        y = x - q * y
        x = t

    # Make x positive
    if (x < 0):
        x = x + m0

    return x


# Driver code
a = 4
m = 99

# Function call
print("Modular multiplicative inverse of ", a,
      "in mod ", m, " is", modInverse(a, m))